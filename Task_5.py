"""Write a method to replace all spaces in a string with '%20'.
Example
Input: "Mr John Smith "
Output: "Mr%20John%20Smith"""

class Replace:  # defining the class
    def string():  # defining the methid
        sentence = input("Enter the string:")
        sentence = sentence.replace(" ", "%20")  # using the replace method, replace the spaces with %20
        print(sentence)


Replace.string()





